</Paragraph>

<SubHeading><InlineIcon><BookCheck /></InlineIcon> Examination</SubHeading>

<Paragraph>
  Kursens examination består av 2 kontrollskrivningar, ett intervjuprojekt och en mindre skrivuppgift.
</Paragraph>

<Paragraph>
    Kontrollskrivningarna HT 2024 bestod av flervalsfrågor och skrivfrågor. Den första kontrollskrivningar hade många flervalsfrågor och någon enstaka skrivfråga medan den andra kontrollskrivningen hade fler skrivfrågor. Kontrollskrivningarna examinerar kursens teori och begrepp. och den andra kontrollskrivningen HT 2024 skulle man analysera ett case.
</Paragraph>

<Paragraph>
    Intervjuprojekt innebär att man i grupper om som mest 5 personer intervjuar någon som jobbar på ett kunskapsintensivt företag. Gruppen får själva hitta någon att intervjua. Gruppen väljer tre av tio teman som ska undersökas och gruppen får sedan själv bestämma vilka frågor som ska ställas under intervjun för att kunna besvara på frågor kring de valda teman. Frågorna man planerar att ställa skickas in som en Pass/Fail-uppgift där man kan få feedback på sina frågor. Projektet lämnas sedan in i form av en skriftlig rapport där man diskuterar hur företaget förhåller sig till de valda teman och kopplar det till relevant teori.
</Paragraph>

<Paragraph>
    Den mindre skrivuppgiften utförs i slutet av kursen och handlar om att reflektera kring vad man lärt sig under kursen.
</Paragraph>

